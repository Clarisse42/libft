int		ft_strequ(char const *s1, char const *s2)
{
	int x;

	x = 0;
	if (s1 && s2)
	{
		while (s1[x] == s2[x] && s1[x] && s2[x])
			x++;
		if (s1[x] == s2[x])
			return (1);
		else
			return (0);
	}
	return (0);
}
