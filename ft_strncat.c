#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	x;
	size_t	y;

	y = 0;
	while (s1[y])
		y++;
	x = 0;
	while (s2[x] && x < n)
	{
		s1[y] = s2[x];
		x++;
		y++;
	}
	s1[y] = '\0';
	return (s1);
}
